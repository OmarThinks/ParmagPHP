function countLinesOfFile($fileDirectory)
{
	$myfile = fopen($fileDirectory, "r") or die("Unable to open file!");
	$counter=0;
	// Output one line until end-of-file
	while(!feof($myfile)) 
	{
  		fgets($myfile);$counter++;
	}
	fclose($myfile);
	return $counter;
}


function countLettersInFile($fileDirectory)
{
	$myfile = fopen($fileDirectory, "r") or die("Unable to open file!");
	$counter=0;
	$line="";
	// Output one line until end-of-file
	while(!feof($myfile)) 
	{
  		$line=fgets($myfile);$counter=$counter+strlen($line);
	}
	fclose($myfile);
	return $counter;
}

